
public class Musteri {
    
    private String musId;
    private String musAdi;
    private String musSoyadi;
    private String musTel;
    private String musAdres;

    public String getMusId() {
        return musId;
    }

    public void setMusId(String musId) {
        this.musId = musId;
    }

    public String getMusAdi() {
        return musAdi;
    }

    public void setMusAdi(String musAdi) {
        this.musAdi = musAdi;
    }

    public String getMusSoyadi() {
        return musSoyadi;
    }

    public void setMusSoyadi(String musSoyadi) {
        this.musSoyadi = musSoyadi;
    }

    public String getMusTel() {
        return musTel;
    }

    public void setMusTel(String musTel) {
        this.musTel = musTel;
    }

    public String getMusAdres() {
        return musAdres;
    }

    public void setMusAdres(String musAdres) {
        this.musAdres = musAdres;
    }
    

}
